from scipy.stats import linregress

def analyze(file, dataset_name, calc_level, column_name):
    if file is not None:
        df = pd.read_csv(file.name)
    elif dataset_name:
        df = bl.load_dataset(dataset_name)
    else:
        return "Please upload a file or select a dataset.", None

    numeric_cols = df.select_dtypes(include=np.number).columns.tolist()
    if not numeric_cols:
        return "No numeric columns found.", None

    col = column_name if column_name in numeric_cols else numeric_cols[0]
    ddof = 1 if calc_level == "sample" else 0
    arr = df[col].dropna().values

    stats_text = f"""
📊 **{{col}}** — {{calc_level.upper()}} Statistics
n = {{len(arr)}}
Mean: {{arr.mean():.4f}}
Median: {{np.median(arr):.4f}}
Std Dev ({{'n-1' if ddof==1 else 'n'}}): {{arr.std(ddof=ddof):.4f}}
Min / Max: {{arr.min():.4f}} / {{arr.max():.4f}}
Skewness: {{float(__import__('scipy').stats.skew(arr)):.4f}}
"""
    fig, ax = plt.subplots(figsize=(8, 4))
    ax.hist(arr, bins="auto", color="#2E86AB", edgecolor="white", alpha=0.85)
    ax.axvline(arr.mean(), color="#D62828", linestyle="--", label=f"Mean: {{arr.mean():.2f}}")
    ax.axvline(np.median(arr), color="#A23B72", linestyle="-.", label=f"Median: {{np.median(arr):.2f}}")
    ax.legend()
    ax.set_title(f"{{col}} Distribution [{{calc_level}}]", fontweight="bold")
    plt.tight_layout()
    return stats_text, fig

